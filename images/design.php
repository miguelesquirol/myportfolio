              <div class="large-12 small-12 medium-12 columns">

                <p>I've been designing pages from more than 20 years, I started with basic html in old Netscape browser in a clunky machine, learned CSSl, moved from flash to HTML5, from tables and iframes to responsive sites, jQuery and CSS3 animations. I can say that I've seen everything, when IE was standard (kidding), when Wordpress was fighting their place as a simple blog platform to become one of the most (if not the most) popular CMS around. I created many Wordpress sites, using templates, frameworks and even some of them from scratch. I had to learn CSS3, jQuery and PHP to keep up to date. </p>
                <p>Since 2009 I started to work as webdesigner professionally, with small projects in an independent manner, designing, coding and hosting. Since 2013 I've been working full time as front end developer. Today is not just a hobby but I create useful pages for people, developing full fledged websites, and working in a company with thousands of clients.</p>
